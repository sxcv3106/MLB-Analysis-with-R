  s4 <- ggplot(San_Francisco_Giants, aes(YEAR,AVG,col=PCT))+labs(title='San Francisco Giants PCT vs AVG in 20 Years')+geom_point(size=5)+scale_colour_gradient(low = 'blue', high = 'red')
