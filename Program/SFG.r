SFG <- function() {
  setwd('D:\\6. Lecture\\2. Sophomore_Second\\3.pm_R_Language\\Project')
  San_Francisco_Giants <- read.csv('San_Francisco_Giants.csv')
  s1 <- ggplot(San_Francisco_Giants, aes(YEAR,HR,col=PCT))+labs(title='San Francisco Giants PCT vs HR in 20 Years')+geom_point(size=5)+scale_colour_gradient(low = 'blue', high = 'red')
